public class User {
    String userName;
    String userLastName;

    public void setUserName(String userName) {
        this.userName = userName;
    }

    public void setUserLastName(String userLastName) {
        this.userLastName = userLastName;
    }

    public String getUserName() {
        return userName;
    }


    public String getUserLastName() {
        return userLastName;
    }


}
